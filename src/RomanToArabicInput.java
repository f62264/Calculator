class RomanToArabicInput {

    public int romanToArabic(String input) {

        int romanToArabicInput = 0;

        switch (input) {
            case "I":
                romanToArabicInput = 1;
                break;
            case "II":
                romanToArabicInput = 2;
                break;
            case "III":
                romanToArabicInput = 3;
                break;
            case "IV":
                romanToArabicInput = 4;
                break;
            case "V":
                romanToArabicInput = 5;
                break;
            case "VI":
                romanToArabicInput = 6;
                break;
            case "VII":
                romanToArabicInput = 7;
                break;
            case "VIII":
                romanToArabicInput = 8;
                break;
            case "IX":
                romanToArabicInput = 9;
                break;
            case "X":
                romanToArabicInput = 10;
                break;

        }
        return romanToArabicInput;
    }
}

